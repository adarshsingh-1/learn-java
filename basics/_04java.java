package basics;

public class _04java {
  public static void main(String[] args) {
    //relational operators
    int a = 10;
    int b = 5;

    // equal to
    System.out.println(a == b); 

    // not equal to
    System.out.println(a != b); 

    // greater than
    System.out.println(a > b); 

    // less than
    System.out.println(a < b); 

    // greater than or equal to
    System.out.println(a >= b);
     
    // less than or equal to
    System.out.println(a <= b);

  }
  
}
