package basics;

public class _07java {
  public static void main(String[] args) {
    //assignment operators

    // simple assignment
    int a = 10; 
    a += 5;     
    System.out.println(a);

    // subtract and assign
    a -= 3;     
    System.out.println(a);

    // multiply and assign
    a *= 2;     
    System.out.println(a);

    // divide and assign
    a /= 4;     
    System.out.println(a);

    // modulus and assign
    a %= 3;     
    System.out.println(a);

    // bitwise AND and assign
    a &= 2;     
    System.out.println(a);

    // bitwise OR and assign
    a |= 1;     
    System.out.println(a);

  }
  
}
