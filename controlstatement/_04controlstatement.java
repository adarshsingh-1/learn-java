package controlstatement;

public class _04controlstatement {
  public static void main(String[] args) {

    // for loop
    for (int i = 1; i <= 5; i++) {
      System.out.println(i);
    }

    // while loop
    int i = 1;
    while (i <= 5) {
      System.out.println(i);
      i++;
    }

    // do-while loop
    int a = 1;
    do {
      System.out.println(a);
      a++;
    } while (a <= 5);

  }

}
